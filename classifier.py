import json
import re
import google.generativeai as genai
from config import GEMINI_API_KEY

genai.configure(api_key=GEMINI_API_KEY)

def classify_query(text):
    """
    Пайдаланушы мәтінін классификациялайды:
    - {"action": "search", "query": "Snickers"} — өнім/мекеме іздеу
    - {"action": "chat"} — AI мен сөйлесу

    Тек 4+ сөзді сұраулар үшін шақырылады.
    Gemini Flash Lite — жылдам әрі арзан.
    """
    model = genai.GenerativeModel('gemini-3.1-flash-lite-preview')

    prompt = f"""Сен халал тамақ тексеретін Telegram бот үшін сұрауды талдайсың.
Сенің жалғыз міндетің: пайдаланушы НЕНІ іздеп жатқанын дәл анықтау.

ПАЙДАЛАНУШЫ ЖАЗДЫ: "{text}"

═══════════════════════════════════
ШЕШІМ АҒАШЫ (осыны орында):
═══════════════════════════════════

ҚАДАМ 1: Пайдаланушы өнім/мекеме халалдығы туралы сұрап жатыр ма?
Белгілері: «халал ма», «харам ба», «тексер», «бар ма», «жарай ма», «рұқсат па»,
           «өнім атауы + сұрақ», «мекеме атауы + сұрақ», «E-код»
→ ИӘ болса: ҚАДАМ 2-ге өт
→ ЖОҚ болса (амандасу, жалпы әңгіме, кеңес): {{"action": "chat"}}

ҚАДАМ 2: Пайдаланушы НЕНІ іздеп жатыр? (мұқият оқы)
Нақты іздеу атауын шығар. Мыналарға назар аудар:

а) Тырнақшадағы сөз бар ма? («пакмир», "snickers") → ол іздеу атауы
б) «X ЕМЕС Y», «X емес Y», «X деп емес Y деп» жазылса → Y іздеу атауы, X елемеу
в) «Y деп аталатын», «Y деп аталады», «атауы Y» → Y іздеу атауы  
г) «осы өнімнің», «сол дүкеннің», «мына нан» → алдыңғы контексттен ат шығар
д) Ешқандай айрықша белгі жоқ болса → сөйлемнің негізгі затын (noun) ал

ҚАДАМ 3: Іздеу атауын тазала
- Тек бренд/өнім/мекеме атауын ғана қалдыр
- «халал», «харам», «өнім», «деген», «сұрайын», «айтшы» — ЖОЙ
- Мүмкіндігінше қысқа: 1-3 сөз

═══════════════════════════════════
МЫСАЛДАР:
═══════════════════════════════════
«пакмир халал ма»                        → {{"action":"search","query":"пакмир"}}
«Snickers жесем болады ма»               → {{"action":"search","query":"Snickers"}}
«tagam емес пакмир деген өнім халал ма»  → {{"action":"search","query":"пакмир"}}
«бұл жолы tagam ды емес пакмир деп аталатын өнім халал ма» → {{"action":"search","query":"пакмир"}}
«KFC-тің осы жердегісі халал ма»        → {{"action":"search","query":"KFC"}}
«E471 қоспасы қауіпті ме»               → {{"action":"search","query":"E471"}}
«маған халал туралы айтшы»              → {{"action":"chat"}}
«ассалаумағалейкум»                     → {{"action":"chat"}}
«қалай жағдай»                          → {{"action":"chat"}}
«Lay's чипсының халал екенін білгім келеді» → {{"action":"search","query":"Lay's"}}
«жи жауап берші, rahat кәмпиті жарай ма» → {{"action":"search","query":"rahat"}}

═══════════════════════════════════
ТЕК JSON ФОРМАТЫНДА ЖАУАП БЕР:
{{"action": "search", "query": "өнім атауы"}}
немесе
{{"action": "chat"}}
═══════════════════════════════════"""

    # Тырнақшадағы сөзді алдын ала шығарамыз — classifier-ге hint береміз
    import re as _re
    quoted = _re.findall(r'[«»""\']([\w\s\-]+)[«»""\']', text)
    hint = ""
    if quoted:
        hint = f"\nЕскерту: Мәтінде тырнақшаға алынған атау бар: {quoted}. Іздеу атауы осы болуы мүмкін."

    try:
        response = model.generate_content(prompt + hint)
        raw = response.text.strip()
        # JSON тазалау
        raw = re.sub(r'```json\s*', '', raw)
        raw = re.sub(r'```\s*', '', raw).strip()
        result = json.loads(raw)
        action = result.get("action", "chat")
        query = result.get("query", "").strip() if action == "search" else ""
        return action, query
    except Exception as e:
        print(f"[classifier] Қате: {e}")
        # Қате болса — тікелей іздеу жіберейік (қауіпсіз fallback)
        return "search", text


def should_classify(text):
    """
    4+ сөз болса классификатор шақырылады.
    1-3 сөз — тікелей іздеу.
    """
    words = text.strip().split()
    return len(words) >= 4
